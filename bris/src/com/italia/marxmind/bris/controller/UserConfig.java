public class UserConfig {

	private String fileName;
	private String themeSelected;
	private String doNotNotifyAgainToday;
	private String versionUsing;
	private String barangay;
	
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getThemeSelected() {
		return themeSelected;
	}
	public void setThemeSelected(String themeSelected) {
		this.themeSelected = themeSelected;
	}
	public String getDoNotNotifyAgainToday() {
		return doNotNotifyAgainToday;
	}
	public void setDoNotNotifyAgainToday(String doNotNotifyAgainToday) {
		this.doNotNotifyAgainToday = doNotNotifyAgainToday;
	}
	public String getVersionUsing() {
		return versionUsing;
	}
	public void setVersionUsing(String versionUsing) {
		this.versionUsing = versionUsing;
	}
	public String getBarangay() {
		return barangay;
	}
	public void setBarangay(String barangay) {
		this.barangay = barangay;
	}
	
	
}
